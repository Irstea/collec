require_once 'modules/classes/eventType.class.php';
$dataClass = new EventType($bdd, $ObjetBDDParam);
$keyName = "event_type_id";
$id = $_REQUEST[$keyName];

switch ($t_module["param"]) {
    case "list":
        /*
         * Display the list of all records of the table
         */
        $vue->set($dataClass->getListe(2), "data");
        $vue->set("param/eventTypeList.tpl", "corps");
        break;
    case "change":
        /*
         * open the form to modify the record
         * If is a new record, generate a new record with default value :
         * $_REQUEST["idParent"] contains the identifiant of the parent record
         */
        dataRead($dataClass, $id, "param/eventTypeChange.tpl");
        break;
    case "write":
        /*
         * write record in database
         */
        $id = dataWrite($dataClass, $_REQUEST);
        if ($id > 0) {
            $_REQUEST[$keyName] = $id;
        }
        break;
    case "delete":
        /*
         * delete record
         */
        dataDelete($dataClass, $id);
        break;
    case "getAjax":
        $_REQUEST["object_type"] == 2 ? $category = "container" : $category = "sample";
        $vue->set($dataClass->getListeFromCategory($category, $_REQUEST["collection_id"]));
        break;
}
